const browser = await chromium.launch({ headless: true });
const p = await browser.newPage({ viewport: { width: 900, height: 760 }, deviceScaleFactor: 2 });
await p.setContent(HTML, { waitUntil: "networkidle" });
mkdirSync("e2e/assets", { recursive: true });
mkdirSync("public", { recursive: true });
const el = await p.locator("body");
await el.screenshot({ path: "e2e/assets/price-sheet.png" });
await browser.close();
copyFileSync("e2e/assets/price-sheet.png", "public/price-sheet.png");

// base64-embed into the API so the serverless FC (no bundled assets) can read it.
const b64 = readFileSync("e2e/assets/price-sheet.png").toString("base64");
mkdirSync("../../apps/api/src/document", { recursive: true });
writeFileSync(
  "../../apps/api/src/document/price-sheet.ts",
  `// AUTO-GENERATED by apps/web/e2e/gen-price-sheet.mjs — a realistic supplier price-sheet image\n` +
    `// (the customer's "attached price sheet"), embedded so the serverless FC can feed it to qwen-vl-plus.\n` +
    `export const PRICE_SHEET_DATA_URL = "data:image/png;base64,${b64}";\n`,
);
console.log(`price-sheet.png generated (${(b64.length / 1024).toFixed(0)}KB base64) -> public/ + apps/api/src/document/price-sheet.ts`);
